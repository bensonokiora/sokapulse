// Transform raw data into organized structure by region and country
const transformLeaguesData = (data) => {
  const result = {
    international: {
      Africa: [],
      Asia: [],
      Europe: [],
      'North & Central America': [],
      'South America': [],
      'Austria & Oceanic': [],
      World: []
    },
    countries: {}
  };

  data.aT.forEach(league => {
    const { country_name, ...leagueData } = league;
    
    // Check if it's an international/continental competition
    if (['Africa', 'Asia', 'Europe', 'North & Central America', 'South America', 'Austria & Oceanic', 'World'].includes(country_name)) {
      result.international[country_name].push({
        league_id: leagueData.league_id,
        league_name: leagueData.league_name,
        league_short_name: leagueData.league_short_name,
        country_name
      });
    } else {
      // Create country array if it doesn't exist
      if (!result.countries[country_name]) {
        result.countries[country_name] = [];
      }
      
      result.countries[country_name].push({
        league_id: leagueData.league_id,
        league_name: leagueData.league_name,
        league_short_name: leagueData.league_short_name
      });
    }
  });

  // Group country leagues by country_name
  countryLeaguesData.aT.forEach(league => {
    const { country_name, ...leagueData } = league;
    
    if (!result.countries[country_name]) {
      result.countries[country_name] = [];
    }
    
    result.countries[country_name].push({
      league_id: leagueData.league_id,
      league_name: leagueData.league_name,
      league_short_name: leagueData.league_short_name
    });
  });

  return result;
};

